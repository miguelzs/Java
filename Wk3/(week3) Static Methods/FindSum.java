/*
 * Super interessting program to find the sum of the int numbers
 * form 1 to 10 inclusive.
 */
public class FindSum {
    public static void main(String[] args) {

        int sum = 0;
        for (int i = 1; i <= 10; i++)
          sum += i;
        System.out.println("Sum from 1 to 10 is " + sum);

    }

}
